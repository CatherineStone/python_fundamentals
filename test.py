# .lower() is used to return the string into lower case.
print("Hello World!".lower())

# .capitalize returns a capitalised version of the string.
print("Hello World!".capitalize())

# .count returns a number value of the amount of characters in a string
print("Hello World!".count("hello"))

# .find returns the value of the specified character in parenthesis.
print("Hello World!".find("Hello"))

# .replace
